import { access, readFile, stat, writeFile } from 'node:fs/promises';
import { constants } from 'node:fs';
import { join } from 'node:path';

const repoRoot = process.cwd();
const manifestPath = join(repoRoot, 'assets', 'manifests', 'commercial-creative-plan.json');
const reportPath = join(repoRoot, 'docs', 'design', 'visual-validation-report.md');

function resolveOutputPath(outputKey) {
  const declared = join(repoRoot, outputKey);
  if (declared.endsWith('.png')) {
    return declared;
  }
  return `${declared}.png`;
}

async function fileExists(path) {
  try {
    await access(path, constants.F_OK);
    return true;
  } catch {
    return false;
  }
}

async function main() {
  const manifestRaw = await readFile(manifestPath, 'utf8');
  const manifest = JSON.parse(manifestRaw);
  const assets = manifest.assets ?? [];

  const missing = [];
  const invalid = [];
  const byType = new Map();
  const byStory = new Map();
  let bytesTotal = 0;

  for (const asset of assets) {
    const outputPath = resolveOutputPath(asset.outputKey);
    const metaPath = outputPath.replace(/\.png$/i, '.meta.json');
    const exists = await fileExists(outputPath);
    const metaExists = await fileExists(metaPath);

    if (!exists) {
      missing.push(outputPath);
      continue;
    }

    const fileStat = await stat(outputPath);
    bytesTotal += fileStat.size;
    if (fileStat.size < 35_000) {
      invalid.push(`${outputPath} (file too small: ${fileStat.size} bytes)`);
    }
    if (!metaExists) {
      invalid.push(`${metaPath} (missing metadata)`);
    }

    byType.set(asset.type, (byType.get(asset.type) ?? 0) + 1);
    const storyKey = asset.storyId ?? 'website';
    byStory.set(storyKey, (byStory.get(storyKey) ?? 0) + 1);
  }

  const lines = [];
  lines.push('# Visual Validation Report');
  lines.push('');
  lines.push(`Generated: ${new Date().toISOString()}`);
  lines.push(`Manifest: \`${manifestPath.replace(`${repoRoot}\\`, '')}\``);
  lines.push('');
  lines.push('## Summary');
  lines.push(`- Total assets in plan: ${assets.length}`);
  lines.push(`- Missing files: ${missing.length}`);
  lines.push(`- Invalid assets: ${invalid.length}`);
  lines.push(`- Total generated bytes: ${bytesTotal.toLocaleString()}`);
  lines.push('');
  lines.push('## Count By Type');
  for (const [type, count] of [...byType.entries()].sort((a, b) => a[0].localeCompare(b[0]))) {
    lines.push(`- ${type}: ${count}`);
  }
  lines.push('');
  lines.push('## Count By Story/Surface');
  for (const [story, count] of [...byStory.entries()].sort((a, b) => a[0].localeCompare(b[0]))) {
    lines.push(`- ${story}: ${count}`);
  }
  lines.push('');

  if (missing.length > 0) {
    lines.push('## Missing Files');
    for (const item of missing.slice(0, 1000)) {
      lines.push(`- ${item.replace(`${repoRoot}\\`, '')}`);
    }
    lines.push('');
  }

  if (invalid.length > 0) {
    lines.push('## Invalid Files');
    for (const item of invalid.slice(0, 1000)) {
      lines.push(`- ${item.replace(`${repoRoot}\\`, '')}`);
    }
    lines.push('');
  }

  lines.push('## Certification');
  if (missing.length === 0 && invalid.length === 0) {
    lines.push('- Status: PASS');
    lines.push('- Evidence: Every planned commercial asset exists with metadata and minimum binary size gate.');
  } else {
    lines.push('- Status: FAIL');
    lines.push('- Evidence: Missing or invalid assets listed above.');
  }

  await writeFile(reportPath, `${lines.join('\n')}\n`, 'utf8');
  if (missing.length > 0 || invalid.length > 0) {
    console.error(`[visual-validation] FAIL - missing=${missing.length}, invalid=${invalid.length}`);
    process.exit(1);
  }

  console.log(`[visual-validation] PASS - validated ${assets.length} assets`);
  console.log(`[visual-validation] Report: ${reportPath}`);
}

main().catch((error) => {
  console.error('[visual-validation] Failed:', error);
  process.exit(1);
});
